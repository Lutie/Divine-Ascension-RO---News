<article id="events">
    <h2 class="major">Events</h2>
    <span class="image main"><img src="images/pic02.jpg" alt="" /></span>
    <p>DARO souhaite proposer de nombreux types d'événements aux joueurs afin de pouvoir couvrir un maximum d'envie.</p>

    <div class="info_frame">
        <h4>GVG - War Of Emperium - 1st Edition</h4>
        <div>Le serveur met à l'honneur la première version des War of Emperium.</div>
        <div>Le nombre de chateaux ouverts dépends du nombre de joueurs connectés et tous les participants sont récompensés d'une façon ou d'une autre, pas seulement les vainqueurs !</div>
        <div>Cet événement se joue en guilde.</div>
    </div>

    <div class="info_frame">
        <h4>GVG - War Of Emperium - dominations</h4>
        <div>L'une des features les plus connus des projets mystic est de retour : Les WOE dominations proposent aux guildes de s'affronter dans une bataille de prise de position stratégique.</div>
        <div>Pour l'occasion DARO innove à l'aide d'une carte bien mieux disposée et plus grande qu'avant. Ici aussi tous les participants sont récompensés d'une façon ou d'une autre, pas seulement les vainqueurs !</div>
        <div>Cet événement se joue en guilde.</div>
    </div>
    
    <div class="info_frame">
        <h4>PVP - Battle Grounds</h4>
        <div>Evidemment les joueurs pourrons en découvre par petits groupes lors des Battle Grounds qu'ils seront en mesures de lancer eux même.</div>
        <div>La nature même des BG seront déterminées dans les prochains mois, le meilleur format sera évidemment retenu !</div>
        <div>Cet événement se joue en groupe déterminé aléatoirement lors des inscriptions.</div>
    </div>

    <div class="info_frame">
        <h4>PVP - Arena</h4>
        <div>L'arène est un événement de Battle Royale où chacun lutte pour sa survie, toutes les classes sont mises à l'honneur avec un système qui récompenses toutes les formes de stratégies.</div>
        <div>Cet événement se joue en solo.</div>
    </div>

    <div class="info_frame">
        <h4>PVE - Invasions</h4>
        <div>Les invasions sont des événements aléatoires qui peuvent survenir en soirée : Les grandes villes du sud de Midgard sont alors la cible de vague de monstres qu'il conviendra de repousser !</div>
        <div>Cet événement se joue avec tous le monde !</div>
    </div>

    <div class="info_frame">
        <h4>PVE - Ragnarok War</h4>
        <div>Cet événement sans début ni fin oppose tous les joueurs dans une guerre contre les forces divines qui cherchent à provoquer la fin du monde !</div>
        <div>La guerre évolue à mesure que les joueurs participent et reviens en arrière lorsque le tension est relâchée.</div>
        <div>Cet événement se joue avec tous le monde !</div>
    </div>

    <div class="info_frame">
        <h4>Event - Peco League</h4>
        <div>Les courses de Pecopeco sont l'un des événements phrases des projets Mystic et cette dernière mouture propose une toute nouvelle version !</div>
        <div>Guidez votre Pecopeco élevé avec amour vers la victoire sur les pistes de courses !</div>
        <div>Cet événement se joue en solo.</div>
    </div>

    <div class="info_frame">
        <h4>Event - Events automatisées</h4>
        <div>Afin d'animer le quotidien de multiples autres événements automatisés sont prévus... Mais la liste sera dévoilée plus tard !</div>
        <div>Ces événements se jouerons surement en solo.</div>
    </div>

</article>